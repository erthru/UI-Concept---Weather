import Foundation
import UIKit

extension UIColor {
    
    class var lightBlue: UIColor {
        return UIColor(red: 66/255, green: 161/255, blue: 245/255, alpha: 1.0)
    }
    
    class var almostWhite: UIColor {
        return UIColor(red: 232/255, green: 231/255, blue: 230/255, alpha: 1.0)
    }
    
}
